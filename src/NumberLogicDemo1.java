
public class NumberLogicDemo1 
{

	public static void main(String[] args) 
	{
		System.out.println(10/2);//quotient
		System.out.println(10%2);//reminder
		System.out.println(11/2);//quotient
		System.out.println(11%2);//reminder

		System.out.println(456789/10);//remove the last digit
		System.out.println(4567895%10);//get the last digit
		//456 7
		System.out.println((456*10)+7);//(number*10)+digit
		//0,1,1,2,3,5,8,13  fibonaccis series
		//prime number
	}

}
