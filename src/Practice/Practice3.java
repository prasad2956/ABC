package Practice;

public class Practice3 
{

	public static void main(String[] args) 
	{
	
		for(int a=1;a<=100;a++) 
		{
			if(a%2==1)
			System.out.println(a);
		}
		for(int a=100;a<=150;a++) 
		{
			if(a%2==1)
			System.out.println(a);
		}
		for(int a=150;a>=100;a--) 
		{
			if(a%2==1)
			System.out.println(a);
		}
		for(int a=100;a>=1;a--) 
		{
			if(a%2==1)
			System.out.println(a);
		}
		
	}

}
