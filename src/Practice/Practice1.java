package Practice;

public class Practice1 
{

	public static void main(String[] args) 
	{
	
		for(int a=1;a<=100;a++)
		{
			System.out.println(a);
		}
		for(int a=100;a<=150;a++)
		{
			System.out.println(a);
		}
	for(int a=100;a>=1;a--)
	{
			System.out.println(a);		
	}
		for(int a=150;a>=100;a--)
		{
			System.out.println(a);
		}
		

	}

}
